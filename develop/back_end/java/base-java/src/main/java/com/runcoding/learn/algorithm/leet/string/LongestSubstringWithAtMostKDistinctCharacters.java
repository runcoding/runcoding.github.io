    public static int lengthOfLongestSubstring(String s) {
        int n = s.length();
        int ans = 0;
        // current index of character
        int[] index = new int[128];
        // try to extend the range [i, j]
        for (int j = 0, i = 0; j < n; j++) {
            char c = s.charAt(j);
            int inde = index[c];
            i = Math.max(inde, i);
            ans = Math.max(ans, j - i + 1);
            index[c] = j + 1;
        }
        return ans;
    }



